#!/usr/bin/env perl

use strict;
use warnings;

use Getopt::Long;
use Pod::Usage;
use MIME::Base64;

use MozRepl;
my $repl = MozRepl->new;
$repl->setup({ log => [qw/fatal error/] });

{
    package Server;
    use POE qw/Component::Server::HTTP/;
    use base qw/Class::Accessor::Fast/;

    sub new {
        my $self = shift->SUPER::new( @_ > 1 ? {@_} : $_[0] );
    }

    sub run {
        my $self = shift;

        POE::Session->create(
            object_states => [
                $self => [qw/_start/],
            ],
        );
        POE::Kernel->run;
    }

    sub _start {
        my ($self, $kernel, $session) = @_[OBJECT, KERNEL, SESSION];

        my $bind = sub {
            my ($sub) = @_;

            sub {
                $self->$sub(@_);
            };
        };

        $kernel->alias_set('main');
        POE::Component::Server::HTTP->new(
            Port           => $self->{port},
            ContentHandler => {
                '/cmd/' => $bind->('handle_key'),
                '/swf/' => $bind->('handle_swf'),
                '/'     => $bind->('handle_root'),
            },
        );

        print "ready to connect\n";
    }

    sub handle_root {
        my ($self, $req, $res) = @_;
        $res->code(200);
        $res->content_type('text/html');
        $res->content(<<"__HTML__");
<html>
<head>
<meta http-equiv="Content-Type" content="text/html; charset=utf-8" />
<title>PSPスライド操作パネル</title>
</head>
<body style="margin:0;padding:0">
<object data="/swf/?total=$self->{total}&max=$self->{max}" width="480" height="272" type="application/x-shockwave-flash"></object>
</body>
</html>
__HTML__
    }

    sub handle_swf {
        my ($self, $req, $res) = @_;

        my $swf = $self->{swf};

        $res->code(200);
        $res->content_type('application/x-shockwave-flash');
        $res->content($swf);
    }

    sub handle_key {
        my ($self, $req, $res) = @_;
        $res->code(200);

        my ($key) = $req->uri->path =~ m!cmd/([^/]+)!;

        if ($key eq 'up') {
            $repl->execute("content.$self->{obj}.go(0)");
        }
        elsif ($key eq 'down') {
            $repl->execute("content.$self->{obj}.go(content.$self->{obj}.pages.length - 1)");
        }
        elsif ($key eq 'right') {
            $repl->execute("content.$self->{obj}.step()");
        }
        elsif ($key eq 'left') {
            $repl->execute("content.$self->{obj}.prev()");
        }

        my $page = $repl->execute("content.$self->{obj}.index") + 1;

        $res->code(200);
        $res->content($page);
    }
}

GetOptions(
    \my %opt,
    qw/help num=s sec=s port=s object=s/
);
pod2usage(0) if $opt{help};

my $server = new Server->new(
    port  => $opt{port}     || 80,
    max   => $opt{num}      || 10,
    total => $opt{sec}      || 300,
    obj   => $opt{object} || 'presentation',
    swf   => decode_base64(do { local $/; <DATA> }),
);
$server->run;

